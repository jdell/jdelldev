        protected override object dataReaderToList(DbDataReader reader)
        {
            try
            {
                System.Collections.Generic.List<Componente> res = new System.Collections.Generic.List<Componente>();
                if (reader != null)
                {
                     while (reader.Read())
                        res.Add((Componente)dataReaderToObject(reader));
                    reader.Close();
                }
                return res;
            }
            catch (Exception ex)
            {
                throw ex;
            }	
