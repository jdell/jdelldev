	internal class componenteDAO:objectDAO
	{
        protected override string getQuery(string conditionWhere)
        {
            string query = "select * from componentes e";
            if (!String.IsNullOrEmpty(conditionWhere.Trim())) query += string.Format(" where {0}", conditionWhere);
            
            return query;
        }

        protected override object dataReaderToObject(System.Data.Common.DbDataReader reader)
		{			
			try
			{
				Componente res = null;
				if (reader!=null)
				{
                    res = new Componente();
                    res.ID= Convert.ToString(reader["IdComponente"]);
                    res.Descripcion = Convert.ToString(reader["Componente"]);
				}
				return res;
			}
			catch(Exception ex)
			{
				throw ex;
			}
		}


        public override object doGetAll(DbCommand command)
        {
            try
            {
                string query = getQuery();
                command.CommandText = query;

                DbDataReader reader = command.ExecuteReader();
                List<Componente> res = (List<Componente>)this.dataReaderToList(reader);
                reader.Close();

                return res;
            }
            catch (Exception ex)
            {
                throw ex;
            }
            finally
            {
                command.Parameters.Clear();
            }
        }

        public override object doGet(DbCommand command, object obj)
        {
            try
            {
                Componente objVO = (Componente)obj;

                string where = " IdComponente = " + this.PARAMETERPREFIX + "IdComponente";
                string query = getQuery(where);
                command.CommandText = query;
                command.Parameters.Add(this.CreateParameter(System.Data.DbType.Int32, "" + this.PARAMETERPREFIX + "IdComponente", objVO.ID, command));

                DbDataReader reader = command.ExecuteReader();
                Componente res = null;
                if (reader.Read())
                    res = (Componente)this.dataReaderToObject(reader);
                reader.Close();

                return res;
            }
            catch (Exception ex)
            {
                throw ex;
            }
            finally
            {
                command.Parameters.Clear();
            }
        }

