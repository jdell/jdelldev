        protected override string getQuery(string conditionWhere)
        {
            string query = "select * from subcuentas e";
            if (!String.IsNullOrEmpty(conditionWhere.Trim())) query += string.Format(" where {0}", conditionWhere);
            
            return query;
        }

        protected override object dataReaderToObject(System.Data.Common.DbDataReader reader)
		{			
			try
			{
				SubCuenta res = null;
				if (reader!=null)
				{
                    res = new SubCuenta();
                    res.Bloqueada = Convert.ToBoolean(reader["Bloqueada"]);
                    res.Codigo = Convert.ToString(reader["Codigo"]);
                    res.ContraPartida = Convert.ToString(reader["Contrapartida"]);
                    res.Descripcion= Convert.ToString(reader["Titulo"]);
                    res.SaldoHabitual= Convert.ToString(reader["SaldoHabitual"]);
				}
				return res;
			}
			catch(Exception ex)
			{
				throw ex;
			}
		}


        public override object doGetAll(DbCommand command)
        {
            try
            {
                string query = getQuery();
                command.CommandText = query;

                DbDataReader reader = command.ExecuteReader();
                List<SubCuenta> res = (List<SubCuenta>)this.dataReaderToList(reader);
                reader.Close();

                return res;
            }
            catch (Exception ex)
            {
                throw ex;
            }
            finally
            {
                command.Parameters.Clear();
            }
        }

        public override object doGet(DbCommand command, object obj)
        {
            try
            {
                SubCuenta objVO = (SubCuenta)obj;

                string where = " Codigo = " + this.PARAMETERPREFIX + "Codigo";
                string query = getQuery(where);
                command.CommandText = query;
                command.Parameters.Add(this.CreateParameter(System.Data.DbType.String, "" + this.PARAMETERPREFIX + "Codigo", objVO.Codigo, command));

                DbDataReader reader = command.ExecuteReader();
                SubCuenta res = null;
                if (reader.Read())
                    res = (SubCuenta)this.dataReaderToObject(reader);
                reader.Close();

                return res;
            }
            catch (Exception ex)
            {
                throw ex;
            }
            finally
            {
                command.Parameters.Clear();
            }
        }

