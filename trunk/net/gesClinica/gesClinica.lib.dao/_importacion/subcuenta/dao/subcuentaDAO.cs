        protected override object dataReaderToList(DbDataReader reader)
        {
            try
            {
                System.Collections.Generic.List<SubCuenta> res = new System.Collections.Generic.List<SubCuenta>();
                if (reader != null)
                {
                     while (reader.Read())
                        res.Add((SubCuenta)dataReaderToObject(reader));
                    reader.Close();
                }
                return res;
